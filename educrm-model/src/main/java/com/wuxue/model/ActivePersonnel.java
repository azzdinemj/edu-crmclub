package com.wuxue.model;

public class ActivePersonnel {
    private String pkActivePersonnel;

    private String name;

    private String phone;

    private String mobile;

    public String getPkActivePersonnel() {
        return pkActivePersonnel;
    }

    public void setPkActivePersonnel(String pkActivePersonnel) {
        this.pkActivePersonnel = pkActivePersonnel;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }
}